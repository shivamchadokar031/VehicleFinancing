package com.example.vehiclefinancing;

public class insertCarD {
    String vt;
    String m;
    String v;
    String s;
    String k;

    public insertCarD(String vt, String m, String v, String s, String k) {
        this.vt = vt;
        this.m = m;
        this.v = v;
        this.s = s;
        this.k = k;
    }

    public String getVt() {
        return vt;
    }

    public String getM() {
        return m;
    }

    public String getV() {
        return v;
    }

    public String getS() {
        return s;
    }

    public String getK() {
        return k;
    }
}
